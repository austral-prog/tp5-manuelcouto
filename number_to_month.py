def number_to_month(month):
    if month==1:
        resu="enero"
    elif month==2:
        resu="febrero"
    elif month==3:
        resu="marzo"
    elif month==4:
        resu="abril"
    elif month==5:
        resu="mayo"
    elif month==6:
        resu="junio"
    elif month==7:
        resu="julio"
    elif month==8:
        resu="agosto"
    elif month==9:
        resu="septiembre"
    elif month==10:
        resu="octubre"
    elif month==11:
        resu="noviembre"
    elif month==12:
        resu="diciembre"
    else:
        resu="error"

    return resu
